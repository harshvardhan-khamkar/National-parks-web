// Helper functions for filtering and searching
export const getRegions = () => {
    return [...new Set(parksData.map(park => park.region))].sort();
};

export const getStates = () => {
    return [...new Set(parksData.map(park => park.state))].sort();
};

export const getFeaturedParks = () => {
    return parksData.filter(park => park.featured);
};

export const getParkById = (id) => {
    return parksData.find(park => park.id === id);
};

export const searchParks = (query) => {
    const lowerQuery = query.toLowerCase();
    return parksData.filter(park =>
        park.name.toLowerCase().includes(lowerQuery) ||
        park.state.toLowerCase().includes(lowerQuery) ||
        park.region.toLowerCase().includes(lowerQuery) ||
        park.famousFor.some(item => item.toLowerCase().includes(lowerQuery))
    );
};

export const filterParks = (filters) => {
    return parksData.filter(park => {
        if (filters.region && park.region !== filters.region) return false;
        if (filters.state && park.state !== filters.state) return false;
        if (filters.wildlife && !park.famousFor.some(item =>
            item.toLowerCase().includes(filters.wildlife.toLowerCase())
        )) return false;
        return true;
    });
};
